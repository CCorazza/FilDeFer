char		*ft_strchr(const char *s, int c)
{
	char	a;
	int		i;

	i = 0;
	a = (char)c;
	while (s[i] != 0 && s[i] != a)
		i++;
	if (s[i] == a)
		return ((char *)&s[i]);
	return (0);
}
