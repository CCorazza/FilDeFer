#include "libft.h"

char			*ft_strcat(char *s1, const char *s2)
{
	int			i;
	int			j;

	i = -1;
	j = ft_strlen(s1);
	while (s2[++i] != '\0')
	{
		s1[j] = s2[i];
		j += 1;
	}
	s1[j] = '\0';
	return (s1);
}
